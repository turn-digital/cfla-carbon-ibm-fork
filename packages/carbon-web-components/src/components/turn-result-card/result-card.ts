import { LitElement, html } from 'lit';
import { prefix } from '../../globals/settings';
import styles from './result-card.scss';
import { property } from 'lit/decorators.js';
import { carbonElement as customElement } from '../../globals/decorators/carbon-element';

import CheckmarkFilled16 from '@carbon/icons/lib/checkmark--filled/16';
import ErrorFilled16 from '@carbon/icons/lib/error--filled/16';

/**
 * Result card.
 *
 * @element cds-result-card
 */

@customElement(`${prefix}-result-card`)
class CDSResultCard extends LitElement {
  @property({ type: String }) label = 'Vērtēšanas rezultāts';
  @property({ type: String }) value = 'Apstiprināts';
  /**
   * Types: approved, cancelled.
   */
  @property({ type: String }) icon = 'approved';

  render() {
    return html`
      <div class="result-card">
        <div class="result-card__wrapper">
          <cds-label-value label=${this.label}></cds-label-value>
          <div class="result-card__icon-box">
            ${this.renderIcon()}
            <div class="result-card__icon-title">${this.value}</div>
          </div>
          <slot></slot>
        </div>
      </div>
    `;
  }

  renderIcon() {
    switch (this.icon) {
      case 'approved':
        return html`<div class="result-card__icon result-card__icon--green">
          ${CheckmarkFilled16()}
        </div>`;
      case 'cancelled':
        return html`<div class="result-card__icon result-card__icon--red">
          ${ErrorFilled16()}
        </div>`;
      default:
        break;
    }
    return null;
  }

  static styles = styles; // `styles` here is a `CSSResult` generated by custom WebPack loader
}

export default CDSResultCard;
